/* =====================================================================
   tutorial-basic-data.js  ―― 基本編の台本（仕様書 20）
   ---------------------------------------------------------------------
   このファイルは「何を、どの順で、どう説明するか」だけを持ちます。
   進め方の仕組みは tutorial-controller.js、
   見た目は tutorial-ui.js が受け持ちます。

   【説明文を書くときに決めたこと】
     ・一度に1つのことだけ言う。詰め込むと、どれも残りません。
     ・「なぜそうするのか」を必ず添える。
       手順だけ覚えても、自分のデッキでは使えないためです。
     ・専門語を先に説明してから使う。
     ・カード名は《》で囲み、盤面のどれを指しているか迷わせない。

   【ステップの形】
     id        … 台本上の名前（仕様書 20.3 のIDと同じ）
     chapter   … 進捗表示に出す区切り（仕様書 22.7）
     pages     … 中央に出す説明。1つが1ページ
     guide     … 操作してもらうときの案内（画面の端に出す）
     allow     … 許可する操作。これ以外は受け付けない
     done      … このステップが終わる合図
     hint      … 間違った操作をしたときに出す一言
     setup     … このステップに入るときに仕込むこと
     warnings  … 通常警告を出すか（仕様書 24）
   ===================================================================== */

